    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tcode;
        int tpin;
       
        
        if("".equals(code.getText())&&"".equals(pin.getText())){
            JOptionPane.showMessageDialog(null,"Any field most by empty","Message",JOptionPane.WARNING_MESSAGE);    
        }
        else{
            tcode=code.getText();
            tpin=Integer.valueOf(pin.getText());
            int i=0;
            while(i<Students.liststudents.size()){
                if((Students.liststudents.get(i).getCode().equals(tcode))&&(Students.liststudents.get(i).getPin()==tpin)){
                    JOptionPane.showMessageDialog(null, "Students "+ Students.liststudents.get(i).getName()+" Login", "Message", JOptionPane.OK_CANCEL_OPTION);
                    code.setText("");
                    pin.setText("");
                    this.dispose();
                    main.scheduleW.setVisible(true);
                    break;
                }
                i++;
            }
            
            int i2=0;
            while(i2<Professor.listprofessor.size()){
            if((Professor.listprofessor.get(i2).getCode().equals(tcode))&&(Professor.listprofessor.get(i2).getPin()==tpin)){
                    JOptionPane.showMessageDialog(null, "Professor "+Professor.listprofessor.get(i2).getName() +" Login", "Message", JOptionPane.OK_CANCEL_OPTION);
                    break;
                }
                i2++;}

            int i3=0;
            while(i3<Coordinator.listcoordinator.size()){
            if((Coordinator.listcoordinator.get(i3).getCode().equals(tcode))&&(Coordinator.listcoordinator.get(i3).getPin()==tpin)){
                    JOptionPane.showMessageDialog(null, "Coordinator "+Coordinator.listcoordinator.get(i3).getName() +" Login", "Message", JOptionPane.OK_CANCEL_OPTION);
                    code.setText("");
                    pin.setText("");
                    this.dispose();
                    main.coordinatorW.setVisible(true);
                    break;
                }
                i3++;}
            if(i==Students.liststudents.size()&&i2==Professor.listprofessor.size()&&i3==Coordinator.listcoordinator.size()){
            JOptionPane.showMessageDialog(null, "This information not register in the system", "Message", JOptionPane.OK_CANCEL_OPTION);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

