    private void codeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codeKeyTyped
            char car = evt.getKeyChar();
        if(code.getText().length()>=9) {
            evt.consume();
        }
        if((car<'0' || car>'9')) {
            evt.consume();
        }
    }//GEN-LAST:event_codeKeyTyped

    private void pinKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pinKeyTyped
        char car = evt.getKeyChar();
        if(pin.getText().length()>=4) {
            evt.consume();
        }
        if((car<'0' || car>'9')) {
            evt.consume();
        }
    }//GEN-LAST:event_pinKeyTyped

